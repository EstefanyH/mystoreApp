package com.hache.mystoreapp.bean;

public class Repartidor {

    private int idRepartidor;
    private String nombreRepartidor;
    private String apellidoPRepartidor;
    private String apellidoMRepartidor;
    private int dniRepartidor;
    private int celularRepartidor;
    private String direcRepartidor;
    private String passRepartidor;

    public int getIdRepartidor() {
        return idRepartidor;
    }

    public void setIdRepartidor(int idRepartidor) {
        this.idRepartidor = idRepartidor;
    }

    public String getNombreRepartidor() {
        return nombreRepartidor;
    }

    public void setNombreRepartidor(String nombreRepartidor) {
        this.nombreRepartidor = nombreRepartidor;
    }

    public String getApellidoPRepartidor() {
        return apellidoPRepartidor;
    }

    public void setApellidoPRepartidor(String apellidoPRepartidor) {
        this.apellidoPRepartidor = apellidoPRepartidor;
    }

    public String getApellidoMRepartidor() {
        return apellidoMRepartidor;
    }

    public void setApellidoMRepartidor(String apellidoMRepartidor) {
        this.apellidoMRepartidor = apellidoMRepartidor;
    }

    public int getDniRepartidor() {
        return dniRepartidor;
    }

    public void setDniRepartidor(int dniRepartidor) {
        this.dniRepartidor = dniRepartidor;
    }

    public int getCelularRepartidor() {
        return celularRepartidor;
    }

    public void setCelularRepartidor(int celularRepartidor) {
        this.celularRepartidor = celularRepartidor;
    }

    public String getDirecRepartidor() {
        return direcRepartidor;
    }

    public void setDirecRepartidor(String direcRepartidor) {
        this.direcRepartidor = direcRepartidor;
    }

    public String getPassRepartidor() {
        return passRepartidor;
    }

    public void setPassRepartidor(String passRepartidor) {
        this.passRepartidor = passRepartidor;
    }

}
